package com.pdg.ticket.model;

public class TicketCorrectNoticeObj {
	private String operator;
	private String leaseName;
	private String field;
	private String leaseNumber;
	private String ticketDate;
	private String ticketNumber;
	private String truckNo;
	private String trailerNos;
	private String remarks;
	private String driverNumber;
	private String date;
	
	public TicketCorrectNoticeObj(){
		
	}
	
	public TicketCorrectNoticeObj(String operator,String leaseName,String field,String leaseNumber,String ticketDate,String ticketNumber
			,String truckNo,String trailerNos,String remarks,String driverNumber,String date){
		this.operator=operator;
		this.leaseName=leaseName;
		this.leaseNumber=leaseNumber;
		this.field=field;
		this.ticketDate=ticketDate;
		this.ticketNumber=ticketNumber;
		this.truckNo=truckNo;
		this.trailerNos=trailerNos;
		this.remarks=remarks;
		this.driverNumber=driverNumber;
		this.date=date;	
	}
	
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getLeaseName() {
		return leaseName;
	}
	public void setLeaseName(String leaseName) {
		this.leaseName = leaseName;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getLeaseNumber() {
		return leaseNumber;
	}
	public void setLeaseNumber(String leaseNumber) {
		this.leaseNumber = leaseNumber;
	}
	public String getTicketDate() {
		return ticketDate;
	}
	public void setTicketDate(String ticketDate) {
		this.ticketDate = ticketDate;
	}
	public String getTicketNumber() {
		return ticketNumber;
	}
	public void setTicketNumber(String ticketNumber) {
		this.ticketNumber = ticketNumber;
	}
	public String getTruckNo() {
		return truckNo;
	}
	public void setTruckNo(String truckNo) {
		this.truckNo = truckNo;
	}
	public String getTrailerNos() {
		return trailerNos;
	}
	public void setTrailerNos(String trailerNos) {
		this.trailerNos = trailerNos;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getDriverNumber() {
		return driverNumber;
	}
	public void setDriverNumber(String driverNumber) {
		this.driverNumber = driverNumber;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
