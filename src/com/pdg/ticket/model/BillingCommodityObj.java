package com.pdg.ticket.model;

public class BillingCommodityObj {
	private BillingCommodityValue _freshWater;
	private BillingCommodityValue _pitWater;
	private BillingCommodityValue _productionW;
	private BillingCommodityValue _invert;
	private BillingCommodityValue _serviceWater;
	private BillingCommodityValue _waterCharge;
	private BillingCommodityValue _disposalCharge;
	private BillingCommodityValue _fuelSurcharge;
	
	public BillingCommodityObj(){
		
	}
	public BillingCommodityObj(BillingCommodityValue fresh,BillingCommodityValue pit,BillingCommodityValue production,
			BillingCommodityValue invert,BillingCommodityValue service,BillingCommodityValue water,BillingCommodityValue disposal,BillingCommodityValue fuel){
		this._freshWater=fresh;
		this._pitWater=pit;
		this._productionW=production;
		this._invert=invert;
		this._serviceWater=service;
		this._waterCharge=water;
		this._disposalCharge=disposal;
		this._fuelSurcharge=fuel;
	}
	
	
	public BillingCommodityValue get_freshWater() {
		return _freshWater;
	}
	public void set_freshWater(BillingCommodityValue _freshWater) {
		this._freshWater = _freshWater;
	}
	public BillingCommodityValue get_pitWater() {
		return _pitWater;
	}
	public void set_pitWater(BillingCommodityValue _pitWater) {
		this._pitWater = _pitWater;
	}
	public BillingCommodityValue get_productionW() {
		return _productionW;
	}
	public void set_productionW(BillingCommodityValue _productionW) {
		this._productionW = _productionW;
	}
	public BillingCommodityValue get_invert() {
		return _invert;
	}
	public void set_invert(BillingCommodityValue _invert) {
		this._invert = _invert;
	}
	public BillingCommodityValue get_serviceWater() {
		return _serviceWater;
	}
	public void set_serviceWater(BillingCommodityValue _serviceWater) {
		this._serviceWater = _serviceWater;
	}
	public BillingCommodityValue get_waterCharge() {
		return _waterCharge;
	}
	public void set_waterCharge(BillingCommodityValue _waterCharge) {
		this._waterCharge = _waterCharge;
	}
	public BillingCommodityValue get_disposalCharge() {
		return _disposalCharge;
	}
	public void set_disposalCharge(BillingCommodityValue _disposalCharge) {
		this._disposalCharge = _disposalCharge;
	}
	public BillingCommodityValue get_fuelSurcharge() {
		return _fuelSurcharge;
	}
	public void set_fuelSurcharge(BillingCommodityValue _fuelSurcharge) {
		this._fuelSurcharge = _fuelSurcharge;
	}
}
