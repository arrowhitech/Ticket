package com.pdg.ticket.model;

public class ConfirmedRunlogObj {
	private int idRunlog;
	private int idTicket;
	private boolean isComfirmedRunTicket;
	private boolean isComfirmedRail;
	private boolean isCorrection;
	private int id;
	
	
	public ConfirmedRunlogObj(int id,int idTicket,boolean isComfirmedRunTicket,boolean isComfirmedRail,boolean isCorrection){
		this.idTicket=idTicket;
		this.isComfirmedRunTicket=isComfirmedRunTicket;
		this.isComfirmedRail=isComfirmedRail;
		this.isCorrection=isCorrection;
		this.setId(id);
		
	}
	
	public ConfirmedRunlogObj(){
		
	}
	
	public int getIdTicket() {
		return idTicket;
	}
	public void setIdTicket(int idTicket) {
		this.idTicket = idTicket;
	}
	public boolean isComfirmedRunTicket() {
		return isComfirmedRunTicket;
	}
	public void setComfirmedRunTicket(boolean isComfirmedRunTicket) {
		this.isComfirmedRunTicket = isComfirmedRunTicket;
	}
	public boolean isComfirmedRail() {
		return isComfirmedRail;
	}
	public void setComfirmedRail(boolean isComfirmedRail) {
		this.isComfirmedRail = isComfirmedRail;
	}
	public boolean isCorrection() {
		return isCorrection;
	}
	public void setCorrection(boolean isCorrection) {
		this.isCorrection = isCorrection;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getIdRunlog() {
		return idRunlog;
	}

	public void setIdRunlog(int idRunlog) {
		this.idRunlog = idRunlog;
	}

	
}
